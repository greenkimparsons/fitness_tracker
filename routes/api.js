const router = require("express").Router();
const Workout = require("../models/Workout.js");

// add exercises to most recent workout plan
router.post("/api/workout", ({ body }, res) => {
    Workout.create(body)
    .then(dbWorkout => {
      res.json(dbWorkout);
    })
    .catch(err => {
      res.status(400).json(err);
    });
});

